package com.ivt.jv44_final.entities;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.Table;

/**
 *
 * @author This PC
 */
@Entity
@Table(name = "guest")
public class GuestEntity extends PersonalEntity{
    
    
    @Column(length = 20)
    private String idCard;
    
    @ManyToOne
    @JoinColumn (name="bookingDetail_id")
    private BookingDetailEntity bookingDetail;

    public GuestEntity() {
    }

    public String getIdCard() {
        return idCard;
    }

    public void setIdCard(String idCard) {
        this.idCard = idCard;
    }

    public BookingDetailEntity getBookingDetail() {
        return bookingDetail;
    }

    public void setBookingDetail(BookingDetailEntity bookingDetail) {
        this.bookingDetail = bookingDetail;
    }
    
    
    
}
